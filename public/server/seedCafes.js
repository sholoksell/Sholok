async function seedCafes() {
  try {
    await mongoose.connect(MONGODB_URI);
    console.log('✅ MongoDB Connected');

    // Clear existing cafes
    await Cafe.deleteMany({});
    console.log('🗑️  Cleared existing cafes');

    // Calculate reviewCount and rating from reviews array
    const cafesWithComputedRatings = bangladeshCafes.map(cafe => {
      if (cafe.reviews && cafe.reviews.length > 0) {
        const total = cafe.reviews.reduce((acc, r) => acc + r.rating, 0);
        cafe.rating = Math.round((total / cafe.reviews.length) * 10) / 10;
        cafe.reviewCount = cafe.reviews.length;
      }
      return cafe;
    });

    const inserted = await Cafe.insertMany(cafesWithComputedRatings);
    console.log(`✅ Seeded ${inserted.length} Bangladesh cafes successfully!`);

    inserted.forEach(c => console.log(`  📍 ${c.name} — ${c.city}, ${c.area} (${c.rating}⭐)`));
  } catch (err) {
    console.error('❌ Seed error:', err);
  } finally {
    await mongoose.disconnect();
    console.log('🔌 MongoDB Disconnected');
  }
}

seedCafes();
