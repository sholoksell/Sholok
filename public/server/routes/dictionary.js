/** Strip common English suffixes to find a stem */
const SUFFIXES = ['tion', 'sion', 'ment', 'ness', 'ings', 'ing', 'ers', 'ies', 'es', 'ed', 'er', 'ly', 's'];

function stripSuffix(word) {
  for (const s of SUFFIXES) {
    if (word.endsWith(s) && word.length - s.length >= 3) {
      return word.slice(0, word.length - s.length);
    }
  }
  return null;
}

/**
 * Lookup a single word: exact → suffix-stripped → null
 * Returns { en, bn } or null
 */
function lookupWord(raw) {
  const k = raw.toLowerCase().trim();
  if (DICT[k]) return { en: k, bn: DICT[k] };

  const stem = stripSuffix(k);
  if (stem && DICT[stem]) return { en: k, bn: DICT[stem] };

  return null;
}

