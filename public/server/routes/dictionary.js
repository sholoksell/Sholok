/**
 * GET /api/dictionary/suggest?q=mob&limit=8
 * Returns up to `limit` { en, bn } pairs whose English key starts with q.
 * Sorted: exact-start first, then alphabetical.
 */
router.get('/suggest', (req, res) => {
  const q     = (req.query.q || '').toLowerCase().trim();
  const limit = Math.min(parseInt(req.query.limit) || 8, 30);

  if (!q) return res.json([]);

  const results = [];
  for (const [en, bn] of Object.entries(DICT)) {
    if (en.startsWith(q)) {
      results.push({ en, bn });
      if (results.length >= limit * 2) break; // collect a bit more, then slice
    }
  }

  // Prioritise shorter / more exact matches
  results.sort((a, b) => a.en.length - b.en.length);

  return res.json(results.slice(0, limit));
});

/**
 * GET /api/dictionary/translate?word=mobile
 * Returns { en, bn, found } for a single word.
 */
router.get('/translate', (req, res) => {
  const word = (req.query.word || '').trim();
  if (!word) return res.status(400).json({ error: 'word parameter required' });

  const hit = lookupWord(word);
  if (hit) return res.json({ ...hit, found: true });

  return res.json({ en: word, bn: null, found: false });
});

/**
 * GET /api/dictionary/transliterate?text=mobile+phone
 * Converts each word in the phrase and returns the full Bangla text.
 * Words not found in the dictionary are returned as-is.
 */
router.get('/transliterate', (req, res) => {
  const text = (req.query.text || '').trim();
  if (!text) return res.status(400).json({ error: 'text parameter required' });

  const words     = text.split(/\s+/);
  const converted = words.map(w => {
    const hit = lookupWord(w);
    return hit ? hit.bn : w;
  });

  return res.json({
    original: text,
    bangla:   converted.join(' '),
    words:    words.map((w, i) => ({ en: w, bn: converted[i] })),
  });
});

/**
 * GET /api/dictionary/search?q=mob&limit=10
 * Broader: returns prefix matches first, then substring matches.
 */
router.get('/search', (req, res) => {
  const q     = (req.query.q || '').toLowerCase().trim();
  const limit = Math.min(parseInt(req.query.limit) || 10, 30);

  if (!q) return res.json([]);

  const exact    = [];
  const prefix   = [];
  const contains = [];

  for (const [en, bn] of Object.entries(DICT)) {
    if (en === q)             exact.push({ en, bn });
    else if (en.startsWith(q)) prefix.push({ en, bn });
    else if (en.includes(q))   contains.push({ en, bn });
  }

  const results = [...exact, ...prefix, ...contains].slice(0, limit);
  return res.json(results);
});

