export const news = Array.from({ length: 500 }).map((_, i) => {
  const rand = seededRandom(i + 500);
  const cat = categories[Math.floor(rand() * categories.length)];
  const dayOffset = Math.floor(rand() * 90);
  const date = new Date();
  date.setDate(date.getDate() - dayOffset);
  const pool = headlinesByCategory[cat];
  const headline = pool[i % pool.length];
  return {
    id: `news-${i + 1}`,
    title: `${headline} #${i + 1}`,
    category: cat,
    excerpt: "বাংলাদেশের ক্রীড়াঙ্গনে নতুন এক অধ্যায়ের সূচনা হলো আজকের এই গুরুত্বপূর্ণ ঘটনার মধ্য দিয়ে। বিস্তারিত জানতে সম্পূর্ণ প্রতিবেদনটি পড়ুন।",
    date: date.toISOString(),
    author: "ক্রীড়া প্রতিবেদক",
    isBreaking: rand() > 0.92,
    isTrending: rand() > 0.85,
    views: Math.floor(rand() * 50000),
    image: "📰",
    readTime: 2 + Math.floor(rand() * 6),
  };
});

export const breakingNews = news.filter((n) => n.isBreaking).slice(0, 8);
export const trendingNews = news.filter((n) => n.isTrending).slice(0, 10);

export const getNewsById = (id) => news.find((n) => n.id === id);
