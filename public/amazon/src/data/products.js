export const getProductById = (id) =>
  products.find((p) => p.id === Number(id));

export const getProductsByCategory = (category) =>
  products.filter((p) =>
    p.category.toLowerCase() === category.toLowerCase()
  );

export const getFeaturedProducts = (count = 12) =>
  products.filter((p) => p.badge).slice(0, count);

export const getBestSellers = (count = 12) =>
  products.filter((p) => p.badge === "Best Seller").slice(0, count);

export const getTodaysDeals = (count = 12) =>
  products.filter((p) => p.discount >= 30).slice(0, count);

export const getNewArrivals = (count = 8) =>
  products.filter((p) => p.badge === "New").slice(0, count);

export const searchProducts = (query, category = null) => {
  const q = query.toLowerCase();
  return products.filter((p) => {
    const matchQuery =
      !q ||
      p.title.toLowerCase().includes(q) ||
      p.brand.toLowerCase().includes(q) ||
      p.category.toLowerCase().includes(q);
    const matchCategory =
      !category || p.category.toLowerCase() === category.toLowerCase();
    return matchQuery && matchCategory;
  });
};

export default products;
