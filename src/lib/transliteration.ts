// ─── Normalise a lookup key ────────────────────────────────────────────────
function normKey(w: string): string {
  return w.toLowerCase().trim();
}

// ─── Core lookup (with common English plural / suffix stripping) ───────────
function lookupWord(word: string): string | null {
  const k = normKey(word);
  if (WORD_DICT[k]) return WORD_DICT[k];

  // Try stripping English plurals / common suffixes
  const suffixes = ['ing', 'ers', 'es', 's', 'ed', 'er'];
  for (const sfx of suffixes) {
    if (k.endsWith(sfx) && k.length - sfx.length >= 3) {
      const stem = k.slice(0, k.length - sfx.length);
      if (WORD_DICT[stem]) return WORD_DICT[stem];
    }
  }
  return null;
}

// ─── Public API ───────────────────────────────────────────────────────────

/**
 * Transliterate a single whitespace-free word.
 * Tries the dictionary first; falls back to Avro phonetic.
 */
export function transliterateWord(word: string): string {
  if (!word.trim()) return word;
  const hit = lookupWord(word);
  if (hit) return hit;
  // Avro phonetic fallback
  return avroParse(word);
}

/**
 * Transliterate a full phrase / sentence word-by-word.
 * Whitespace tokens are preserved unchanged.
 */
export function transliterateText(text: string): string {
  if (!text.trim()) return text;
  return text
    .split(/(\s+)/)
    .map(token => (/^\s+$/.test(token) ? token : transliterateWord(token)))
    .join('');
}

/**
 * Return dictionary suggestions for an incomplete word (prefix search).
 * @param partial  - what the user has typed so far
 * @param limit    - maximum results (default 6)
 * Returns array of { en, bn } pairs sorted by key length (shortest first).
 */
export function getSuggestions(
  partial: string,
  limit = 6,
): Array<{ en: string; bn: string }> {
  const k = normKey(partial);
  if (k.length < 2) return [];

  return Object.entries(WORD_DICT)
    .filter(([key]) => key.startsWith(k))
    .sort((a, b) => a[0].length - b[0].length)
    .slice(0, limit)
    .map(([en, bn]) => ({ en, bn }));
}

