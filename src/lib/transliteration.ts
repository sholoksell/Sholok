/**
 * Given a raw user query and inputMode, return the terms to send to the
 * search backend:
 *   - q:    the primary search term (Bangla if BN mode, else raw)
 *   - qEn:  the original English term to also search (only in BN mode)
 */
export function buildSearchTerms(
  raw: string,
  inputMode: 'BN' | 'EN',
): { q: string; qEn?: string } {
  if (inputMode !== 'BN' || !raw.trim()) return { q: raw };
  const bn = transliterateText(raw.trim());
  const q = bn !== raw.trim() ? bn : raw;
  return { q, qEn: raw.trim() };
}
